            <article>
                <p>Com 39 anos de existência, a Compasso nasceu  na capital cearense com o propósito de revolucionar o mercado de mídia exterior. Mantendo o propósito e buscando inovação, a empresa investe, atualmente, em uma tecnologia que irá mensurar os dados sobre o consumo da mídia OOH. Segundo dados da Kantar Ibope Media, a mídia OOH é o segundo meio mais consumido do país e atinge 89% da população. Especificamente em Fortaleza, a penetração do meio chega a 93%. Vendo essa ascensão, a Compasso viu a oportunidade de entregar um diferencial na mídia offline: os dados.</p>
                <p>“Estamos focados agora em trazer para o mundo offline as métricas do mundo online. Isso através de uma tecnologia que envolve hardwares de última geração e softwares que usam machine learning. Esse ecossistema mapeia os celulares que trafegam nas vias e captam seus sinais. Associado a parcerias com empresas internacionais desenvolvedoras de aplicativos, temos acesso a dados básicos, sem ferir a LGPD, demográficos dos usuários. Assim podemos qualificar o público que trafega em determinada região, mesmo sem saber quem são individualmente”, detalha Jansen Araujo, Diretor Geral da organização.</p>
                <p>Com essa tecnologia, a Compasso terá um papel colaborativo com marcas e empresas que decidirem anunciar nesse formato de mídia, uma vez que a empresa irá disponibilizar um mapeamento completo das áreas que possibilitam uma entrega mais estratégica para o anunciante.</p>
                <p>“Uma vez que teremos dados estatísticos e demográficos de vários locais mapeados, podemos escolher e sugerir locais de divulgação mais adequados para o público-alvo das empresas ou de suas campanhas específicas. Então marcas que se comunicam com determinado gênero, faixa etária ou outro filtro terão maior sucesso em suas campanhas. Essa tecnologia trará um grande salto de qualidade e assertividade para nossos clientes e certamente será um marco de inovação para o setor no Norte/Nordeste. Entendemos que essa diferenciação será lembrada pelo mercado em seus planejamentos de mídia”, declara Jansen entusiasmado. Com essa tecnologia, a Compasso terá um papel colaborativo com marcas e empresas que decidirem anunciar nesse formato de mídia, uma vez que a empresa irá disponibilizar um mapeamento completo das áreas que possibilitam uma entrega mais estratégica para o anunciante.</p>
                <p>A Compasso oferece soluções voltadas para mídia exterior, produção digital e comunicação visual. Descrevendo essa evolução ao longo dos anos, Jansen comenta que a empresa diversificou o seu portfólio para atender as necessidades dos clientes. A empresa acumula 16 prêmios como destaque empresarial na categoria outdoor, o que sinaliza seu compromisso em pensar uma comunicação cada vez mais inovadora.</p>
                <p>Essa excelência é reflexo da dedicação com os colaboradores que contam com processos de capacitação, principalmente para essa nova tecnologia que será adicionada na rotina de entrega da empresa.</p>
                <p>“A tecnologia é nova e entraremos em breve na fase de implementação e teste. De modo que toda nossa equipe estará apta para ajudar nossos clientes ainda mais na efetiva escolha de seus locais de divulgação”, conta Jansen.</p>
                <p>Dessa forma, a empresa segue rumo a melhorias se adequando às novas necessidades das marcas e consumidores. Além disso, a empresa investiu em um novo site e embarcou no fortalecimento da marca no universo digital. Por fim, Jansen ressalta a importância da mídia exterior para a comunicação local e celebra o Dia do Outdoor.</p>
                <p>“O outdoor é um dos maiores meios de exibição de mensagens em função do seu tamanho, leitura rápida e de fixação objetiva. Tem um papel fundamental no plano de comunicação das empresas de qualquer porte. Nada mais justo que ter um dia de homenagem“, finaliza Jansen.</p>
